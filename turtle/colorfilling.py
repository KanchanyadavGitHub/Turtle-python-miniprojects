import turtle
s=turtle.getscreen()
s.title("colorfilling")
t2=turtle.Turtle()
t2.ht()
t2.penup()
t2.goto(150,150)
t2.pendown()
t2.begin_fill()
t2.pensize(5)
t2.pencolor("black")
t2.fillcolor("lightblue")
t2.goto(-200,150)
t2.goto(-200,-200)
t2.goto(150,-200)
t2.goto(150,150)
t2.end_fill()
t1=turtle.Turtle()
t1.ht()
t1.pensize(5)
t1.pencolor("purple")
t1.penup()
t1.fillcolor("red")
t1.goto(100,100)
t1.pendown()
t1.begin_fill()
t1.goto(0,100)
t1.goto(0,0)
t1.goto(100,0)
t1.goto(100,100)
t1.end_fill()
t1.penup()
t1.goto(-50,0)
t1.pendown()
t1.begin_fill()
t1.fillcolor("yellow")
t1.goto(-50,100)
t1.goto(-150,100)
t1.goto(-150,0)
t1.goto(-50,0)
t1.end_fill()

t1.penup()

t1.goto(-50,-50)
t1.pendown()
t1.begin_fill()
t1.fillcolor("green")
t1.goto(-50,-150)
t1.goto(-150,-150)
t1.goto(-150,-50)
t1.goto(-50,-50)
t1.end_fill()
t1.penup()
t1.goto(0,-50)

t1.pendown()
t1.begin_fill()
t1.fillcolor("blue")
t1.goto(0,-150)
t1.goto(100,-150)
t1.goto(100,-50)
t1.goto(0,-50)
t1.end_fill()










s.mainloop()
